import java.util.Scanner;
class Num{
    public static void main(String args[])
    {
        Scanner sk = new Scanner(System.in);
        System.out.print("Enter the Number :");
        int n = sk.nextInt();

        for(int i=1;i<=n;i++){
            for(int j=1;j<=i;j++){
                System.out.print(j);
            }
            System.out.println();
        }
    }
}                 

